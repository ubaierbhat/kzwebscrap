def transliterate_to_english(ks_word):
    en_word = ""
    for c in ks_word:
        if c in ks_en_char_map:
            en_word = en_word + ks_en_char_map[c]
        else:
            f = open('missing.txt', 'a', encoding='utf-8')
            new_line = f'\"{c}\": "",\n'
            f.write(new_line)
            f.close()

    return en_word


