def transliterate(ks_word):
    en_word = ""
    for c in ks_word:
        if c in dictionary:
            en_word = en_word + dictionary[c]
        else:
            f = open('missing.txt', 'a', encoding='utf-8')
            new_line = f'\"{c}\": "",\n'
            f.write(new_line)
            f.close()

    return en_word
